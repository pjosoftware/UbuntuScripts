#!/bin/bash
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

for script in "$SCRIPT_DIR"/*.sh; do
  if [[ "$(basename "$script")" != "0000_RunAll.sh" ]]; then
    echo -e "\033[0;32m===============================================================================\033[0m"
    echo -e "\033[0;32mRunning $(basename "$script")...\033[0m"
    echo -e "\033[0;32m===============================================================================\033[0m"
    bash "$script"
    echo -e "\033[0;36m===============================================================================\033[0m"
    echo -e "\033[0;36mFinished running $(basename "$script")!\033[0m"
    echo -e "\033[0;36m===============================================================================\033[0m"
  fi
done