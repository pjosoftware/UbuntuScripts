#!/bin/bash

for script in *.sh; do
  if [[ "$script" != "0000_RunAll.sh" ]]; then
    echo -e "\033[0;32m==============================================================================="
    echo  -e "\033[0;32mRunning $script..."
    echo  -e "\033[0;32m==============================================================================="
    bash "$script"
    echo  -e "\033[0;36m==============================================================================="
    echo  -e "\033[0;36mFinished running $script!"
    echo  -e "\033[0;36m==============================================================================="
  fi
done

