#!/bin/bash

for script in *.sh; do
  if [[ "$script" != "0000_RunAll.sh" ]]; then
    echo "\033[0;32m==============================================================================="
    echo "\033[0;32mRunning $script..."
    echo "\033[0;32m==============================================================================="
    bash "$script"
    echo "\033[0;36m==============================================================================="
    echo "\033[0;36mFishished running $script!"
    echo "\033[0;36m==============================================================================="
  fi
done

